package br.com.atm;

public class Titular {
	private String nome;
	private String email;
	    
	public Titular(String nome, String email){
		this.nome = nome;
	    this.email = email;
	        
	}

	public String getNome() {
		return nome;
	}

	public String getEmail() {
		return email;
	}

	
	
	

}
